// 152. Creating a Stack with an Array
let stack = [];
stack.push('google');

stack.push('instagram');

stack.push('youtube');

stack.pop();

stack.pop();

stack.push('amazon');

stack.pop();

stack.unshift('create new file');

stack.unshift('resized file');

stack.unshift('cloned out wrinkle');

stack.shift();

console.log(stack);
